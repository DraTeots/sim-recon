extern double mom_table[Nxbins][Nybins];

//----------------------
// MomentumFromTableQuick
//----------------------
double MomentumFromTableQuick(double Ro, double theta)
{
	int ix = (int)((theta-Ymin)/bin_width_y + 0.5);
	int iy = (int)((Ro/sin(theta)-Xmin)/bin_width_x + 0.5);
	
	if(ix<0)ix=0;
	if(ix>=Nxbins)ix=Nxbins-1;
	if(iy<0)iy=0;
	if(iy>=Nybins)iy=Nybins-1;
	
	return mom_table[ix][iy];
}

//----------------------
// MomentumFromTable
//----------------------
double MomentumFromTable(double Ro, double theta)
{
	double x = (theta-Xmin)/bin_width_x;
	double y = (Ro/sin(theta)-Ymin)/bin_width_y;
	int ixlow = (int)floor(x);
	int iylow = (int)floor(y);

	// Take weighted average of surrounding points
	double wtot = 0.0;
	double p = 0.0;
	for(int i=ixlow; i<=ixlow+1; i++){
		if(i<0)continue;
		if(i>=Nxbins)continue;
		for(int j=iylow; j<=iylow+1; j++){
			if(j<0)continue;
			if(j>=Nybins)continue;
			
			double dx = (double)i - x;
			double dy = (double)j - y;
			double r2 = dx*dx + dy*dy;
			if(r2 < 1.0E-6)return mom_table[i][j];
			
			p += mom_table[i][j]/r2;
			wtot += 1.0/r2;
		}
	}
	
	return p/wtot;
}

